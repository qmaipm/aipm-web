export function getCase(slug: string): Case {
  const c = ALL_CASES.find((x) => x.slug === slug);
  if (!c) throw new Error(`未知案例 slug: ${slug}`);
  return c;
}

// 旗舰样板(当前仅一篇,自营标杆)
export function getFlagship(): Case | undefined {
  return ALL_CASES.find((c) => c.flagship);
}

// 卡片流:全部非旗舰案例(默认按登记顺序,新案例置顶时插到前面即可)
export const CARD_CASES: Case[] = ALL_CASES.filter((c) => !c.flagship);

// 相关案例:同行业优先 → 同场景标签 → 其余;不含旗舰、不含自身
export function getRelated(slug: string, n = 3): Case[] {
  const self = getCase(slug);
  const selfTags = CASE_TAGS[slug];
  const rest = ALL_CASES.filter((c) => c.slug !== slug && !c.flagship);
  const score = (c: Case) => {
    const t = CASE_TAGS[c.slug];
    if (!t || !selfTags) return 0;
    let s = t.industryTag === selfTags.industryTag ? 2 : 0;
    if (t.useCases.some((u) => selfTags.useCases.includes(u))) s += 1;
    return s;
  };
  return [...rest].sort((a, b) => score(b) - score(a)).slice(0, n);
}

// 由已发布案例实际使用的标签动态生成筛选值(某标签被用到才出现)
const SIZE_ORDER: SizeTag[] = ["初创企业", "中小企业", "大型企业"];
export function getFacets(cases: Case[] = CARD_CASES) {
  const industries: string[] = [];
  const useCases: string[] = [];
  const sizes: SizeTag[] = [];
  for (const c of cases) {
    const t = CASE_TAGS[c.slug];
    if (!t) continue;
    if (!industries.includes(t.industryTag)) industries.push(t.industryTag);
    for (const u of t.useCases) if (!useCases.includes(u)) useCases.push(u);
    if (!sizes.includes(t.sizeTag)) sizes.push(t.sizeTag);
  }
  sizes.sort((a, b) => SIZE_ORDER.indexOf(a) - SIZE_ORDER.indexOf(b));
  return { industries, useCases, sizes };
}
